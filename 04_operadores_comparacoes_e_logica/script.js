

//operadores aritméticos
console.log(1 + 1); //soma
console.log(1 - 1); //subtração
console.log(1 * 2); //multiplicação
console.log(2 / 2); //divisão
console.log(10 % 3); //resto da divisão, no caso seria 1

//operadores relacionas: Comparadores / Comparaações obrigatórias
console.log(2 == "2"); //true! compara se o "caracter" confere com o escolhido, ignorando tipos, apenas aceitando (Eles parecem a mesma coisa?)
console.log(2 === "2"); //false! compara o "valor" da variável, seu dado dentro e seu tipo(string, number...) (ELes são exatamente a mesma coisa?)
console.log(2 > 1); //true! verifica se o valor é maior do que o segundo dado
console.log (2 < 1); //false! verifque seo valor é menor que o segundo dado
console.log(10 >= 10); // true! verifica se 10 é maior ou igual a 10
console.log(7 <= 8);   // true! verofica se 7 é menor ou igual a 8
console.log(2 != "2"); //false! != inverte o  valor, se eles são iguais, retorna false
console.log(2 !== "2"); //true! valores iguais mas tipos diferentes (number x string) (ele verifica se valores ou tipos sao diferentes)

//operadores lógicos
console.log(true && false); //"E" lógico, precisa ser true e false
console.log(true || false); //"OU" lógico, pode ser tanto um quanto outro
console.log(!true); //"Não" lógico, inverte o valor

